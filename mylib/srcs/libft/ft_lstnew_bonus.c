#include "../../includes/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_list;

	new_list = (t_list *)malloc(sizeof(t_list));
	if (new_list == 0)
		return (0);
	new_list->next = 0;
	new_list->content = content;
	return (new_list);
}
